#include "libft.h"

char    *ft_strchr(const char *s, int c)
{
    int i;
    int j;
    i = 0;
    j = 0;

    while(s[i])
    {
        if (s[i] = c)
        {
            j = i;
        }
        if (j > 0 && s[i] = '\0')
            return (s[j]);
        i++;
    }
    return (0);
}